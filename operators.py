# Arithmetic Operators
a, b = 10, 5
print("Addition is : ", a + b)
print("Subtraction is : ", a - b)
print("Multiplication is : ", a * b)
print("Division is : ", a / b)
print("Mod is : ", a % b)
print("Exponent is : ", a ** b)
print("Floor div also know as integer division: ", a // b)

# Assignment Operators
x = y = z = 10
print(x, y, z)

# Comparison Operators ==, !=, <, >, <=, >=
g = 10
s = 98
print(g == s)
print(g != s)
print(g <= s)
print(g >= s)

# Logical Operators
d = 70
f = 30
print(d == 10 and f == 30)
print(d == 10 or f == 30)

